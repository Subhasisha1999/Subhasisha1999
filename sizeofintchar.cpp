#include<iostream>
using namespace std;
int main(){
    int a;
    char b;
    double c;
    float d;
    long long e;
    cout<<"size  of integer : "<<sizeof(a)<<endl;
    cout<<"size  of  char : "<<sizeof(b)<<endl;
    cout<<"size  of double : "<<sizeof(c)<<endl;
    cout<<"size  of float : "<<sizeof(d)<<endl;
    cout<<"size  of long long : "<<sizeof(e)<<endl;
    //cout<<"size  of integer : "<<sizeof(a);
    return 0;
}
